    private void setarDadosTela() {
        jTF_Nome.setText(nome);
        jTF_Cpf.setText(cpf);
        jTF_Email.setText(email);

        if (this.genero.equalsIgnoreCase("Masculino")) {
            jRB_Masculino.setSelected(true);
        } else if (this.genero.equalsIgnoreCase("Feminino")) {
            jRB_Feminino.setSelected(true);
        }

        if (dataNascimento != null) {
            jS_Dia.setValue(dataNascimento.getDayOfMonth());
            jS_Mes.setValue(dataNascimento.getMonthValue());
            jS_Ano.setValue(dataNascimento.getYear());
        }
        jCB_Curso.setSelectedItem(curso);
//        jCB_Curso.setSelectedIndex(2);
    }

